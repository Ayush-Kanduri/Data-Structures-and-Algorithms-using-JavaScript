/**
    * @description Converts the key value pair Array to an Object
    * @timeComplexity O(n)
    * @spaceComplexity O(n)
    * @example
        console.log(
            Object.fromEntries([
                ["a", 1],
                ["b", 2],
                ["c", 3],
                ["d", 4],
            ])
        );

        Output:
        {a: 1, b: 2, c: 3, d: 4}
**/

console.log(
	Object.fromEntries([
		["a", 1],
		["b", 2],
		["c", 3],
		["d", 4],
	])
);
